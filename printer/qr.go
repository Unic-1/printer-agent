package printer

func (b *Builder) QR(data string) {
	b.AlignCenter()

	// Model
	b.buf = append(b.buf, 0x1D, 0x28, 0x6B, 0x04, 0x00, 0x31, 0x41, 0x32, 0x00)

	// Size
	b.buf = append(b.buf, 0x1D, 0x28, 0x6B, 0x03, 0x00, 0x31, 0x43, 0x06)

	// Error correction
	b.buf = append(b.buf, 0x1D, 0x28, 0x6B, 0x03, 0x00, 0x31, 0x45, 0x30)

	// Data
	l := len(data) + 3
	b.buf = append(b.buf,
		0x1D, 0x28, 0x6B,
		byte(l), byte(l>>8),
		0x31, 0x50, 0x30,
	)
	b.buf = append(b.buf, []byte(data)...)

	// Print
	b.buf = append(b.buf, 0x1D, 0x28, 0x6B, 0x03, 0x00, 0x31, 0x51, 0x30)

	b.Feed(2)
}
